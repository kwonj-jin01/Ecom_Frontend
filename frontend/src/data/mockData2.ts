// Helper functions
export const getProductsByCategory = (category) => {
  return products.filter(product => product.category === category);
};

export const getProductsOnSale = () => {
  return products.filter(product => product.isOnSale);
};

export const getProductsByPriceRange = (min: number, max: number) => {
  return products.filter(product => product.price >= min && product.price <= max);
};

export const getProductsInStock = () => {
  return products.filter(product => product.inStock);
};

export const getProductsByPromotion = (promotion) => {
  return products.filter(product => product.promotion === promotion);
};

export const searchProducts = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return products.filter(product => 
    product.name.toLowerCase().includes(term) ||
    product.category.toLowerCase().includes(term) ||
    product.season.toLowerCase().includes(term) ||
    (product.promotion && product.promotion.toLowerCase().includes(term))
  );
};

export const filterProducts = (filters) => {
  let filtered = [...products];

  if (filters.categories && filters.categories.length > 0) {
    filtered = filtered.filter(product => filters.categories.includes(product.category));
  }

  if (filters.sizes && filters.sizes.length > 0) {
    filtered = filtered.filter(product => 
      product.sizes.some(size => filters.sizes.includes(size))
    );
  }

  if (filters.colors && filters.colors.length > 0) {
    filtered = filtered.filter(product => 
      product.colors.some(color => filters.colors.includes(color))
    );
  }

  if (filters.seasons && filters.seasons.length > 0) {
    filtered = filtered.filter(product => filters.seasons.includes(product.season));
  }

  if (filters.priceRange) {
    filtered = filtered.filter(product => 
      product.price >= filters.priceRange.min && product.price <= filters.priceRange.max
    );
  }

  if (filters.onSale) {
    filtered = filtered.filter(product => product.isOnSale);
  }

  if (filters.inStock) {
    filtered = filtered.filter(product => product.inStock);
  }

  if (filters.searchTerm) {
    const term = filters.searchTerm.toLowerCase();
    filtered = filtered.filter(product => 
      product.name.toLowerCase().includes(term) ||
      product.category.toLowerCase().includes(term) ||
      product.season.toLowerCase().includes(term) ||
      (product.promotion && product.promotion.toLowerCase().includes(term))
    );
  }

  return filtered;
};